C     FUNCTION Ctq4Pd (Iset, Iparton, X, Q, Irt) 
C         returns the PROBABILITY density for a GIVEN flavor;
C     SUBROUTINE Ctq4Pds(Iset, Pdf, XX, QQ, Irt)
